#include "libftprintf.h"

int ft_putnbr_rv(int n, int fd)
{
	static int size = 0;

	if (n < 0 && n > -10)
	{
		write(fd, "-", 1);
		size++;
	}
	if (n / 10 != 0)
		ft_putnbr_rv(n / 10, fd);
	write(fd, &"0123456789"[n % 10 * (n < 0 ? -1 : 1)], 1);
	size++;
	return (size);
}
